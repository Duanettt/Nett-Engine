void Block::Init()
{
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
}

void Block::SetPosition(glm::vec3 blockPosition)
{
    blockModel = glm::mat4(1.0f);
    blockModel = glm::translate(blockModel, blockPosition);
}

void Block::SetScale(float scale)
{
    blockModel = glm::scale(blockModel, glm::vec3(scale));
}

void Block::Draw(Shader& shader, glm::mat4 projection, glm::mat4& view)
{
        shader.use();
        shader.setMat4("view", view);
        shader.setMat4("projection", projection);
        // cubes
        glBindVertexArray(VAO);
        shader.setMat4("model", blockModel);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
}
