void Block::Init()
{
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
}

void Block::InitInstanced(std::vector<glm::mat4> modelMatrices)
{
    unsigned int instanceVBO;
    glGenBuffers(1, &instanceVBO);
    glBindBuffer(GL_ARRAY_BUFFER, instanceVBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(glm::mat4) * modelMatrices.size(), modelMatrices.data(), GL_STATIC_DRAW);
    glBindBuffer(GL_ARRAY_BUFFER, 0);

    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glBindVertexArray(VAO);
    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(cubeVertices), &cubeVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));

    glEnableVertexAttribArray(2);
    glBindBuffer(GL_ARRAY_BUFFER, instanceVBO);
    // Each model matrix consists of 4 vec4s, so you need 4 attribute locations
    for (int i = 0; i < 4; i++) {
        glEnableVertexAttribArray(2 + i);
        glVertexAttribPointer(2 + i, 4, GL_FLOAT, GL_FALSE, sizeof(glm::mat4), (void*)(i * sizeof(glm::vec4)));
        glVertexAttribDivisor(2 + i, 1); // Update per instance
    }

    glBindVertexArray(0);
}

void Block::SetPosition(glm::vec3 blockPosition)
{
    blockModel = glm::mat4(1.0f);
    blockModel = glm::translate(blockModel, blockPosition);
}

void Block::SetScale(float scale)
{
    blockModel = glm::scale(blockModel, glm::vec3(scale));
}

void Block::Draw(Shader& shader, glm::mat4 projection, glm::mat4& view)
{
        shader.use();
        // Lighting uniforms
        shader.setVec3("objectColor", 1.0f, 0.5f, 0.31f);
        shader.setVec3("lightColor", 1.0f, 1.0f, 1.0f);
        shader.setVec3("lightPos", 1.2f, 1.0f, 2.0f);

        // Coordinate system uniforms
        shader.setMat4("view", view);
        shader.setMat4("projection", projection);
        // cubes
        glBindVertexArray(VAO);
        shader.setMat4("model", blockModel);
        glDrawArrays(GL_TRIANGLES, 0, 36);
        glBindVertexArray(0);
}

void Block::DrawInstanced(Shader& shader, glm::mat4 projection, glm::mat4& view, int instanceCount) {
    shader.use();

    // Lighting uniforms
    shader.setVec3("objectColor", 1.0f, 0.5f, 0.31f);
    shader.setVec3("lightColor", 1.0f, 1.0f, 1.0f);
    shader.setVec3("lightPos", 20.2f, 30.0f, 3.0f);

    // Coordinate system uniforms
    shader.setMat4("view", view);
    shader.setMat4("projection", projection);

    glBindVertexArray(VAO);
    // Draw all instances
    glDrawArraysInstanced(GL_TRIANGLES, 0, 36, instanceCount);

    glBindVertexArray(0);
}
